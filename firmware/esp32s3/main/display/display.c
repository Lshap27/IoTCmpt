static void fb_fill(uint16_t color) {
    for (int y = 0; y < TFT_H; y++) {
        for (int x = 0; x < TFT_W; x++) {
            s_fb[y][x] = color;
        }
    }
}

static void fb_rect(int x, int y, int w, int h, uint16_t color) {
    if (x < 0) {
        w += x;
        x = 0;
    }
    if (y < 0) {
        h += y;
        y = 0;
    }
    if (x + w > TFT_W) {
        w = TFT_W - x;
    }
    if (y + h > TFT_H) {
        h = TFT_H - y;
    }
    if (w <= 0 || h <= 0) {
        return;
    }

    for (int row = y; row < y + h; row++) {
        for (int col = x; col < x + w; col++) {
            s_fb[row][col] = color;
        }
    }
}

static void fb_hline(int x, int y, int w, uint16_t color) {
    fb_rect(x, y, w, 1, color);
}

static void fb_draw_char(int x, int y, char c, uint16_t fg, uint16_t bg) {
    if (c < 32 || c > 126) {
        c = '?';
    }
    const uint8_t *glyph = font_8x16[c - 32];
    for (int row = 0; row < 16; row++) {
        uint8_t bits = glyph[row];
        for (int col = 0; col < 8; col++) {
            int px = x + col;
            int py = y + row;
            if (px >= 0 && px < TFT_W && py >= 0 && py < TFT_H) {
                s_fb[py][px] = (bits & 0x80) ? fg : bg;
            }
            bits <<= 1;
        }
    }
}

static void fb_draw_string(int x, int y, const char *str, uint16_t fg, uint16_t bg) {
    while (*str) {
        fb_draw_char(x, y, *str, fg, bg);
        x += 8;
        str++;
    }
}

static void fb_draw_string_center(int y, const char *str, uint16_t fg, uint16_t bg) {
    int len = (int)strlen(str);
    int x = (TFT_W - len * 8) / 2;
    fb_draw_string(x, y, str, fg, bg);
}

static void tft_flush(void) {
    uint8_t data[4];
    data[0] = 0x00;
    data[1] = 0x02;
    data[2] = 0x00;
    data[3] = 0x81;
    tft_cmd(ST7735_CASET);
    tft_data(data, 4);

    data[0] = 0x00;
    data[1] = 0x01;
    data[2] = 0x00;
    data[3] = 0x80;
    tft_cmd(ST7735_RASET);
    tft_data(data, 4);

    tft_cmd(ST7735_RAMWR);
    tft_dc(1);
    tft_cs(0);
    tft_spi_send((const uint8_t *)s_fb, TFT_W * TFT_H * 2);
    tft_cs(1);
}

/* ── 初始化 ────────────────────────────────────────────────────────── */

esp_err_t display_init(void) {
    if (!CONFIG_APP_DISPLAY_ENABLED) {
        return ESP_ERR_INVALID_STATE;
    }

    gpio_config_t io = {
        .pin_bit_mask = (1ULL << CONFIG_APP_TFT_CS_GPIO) | (1ULL << CONFIG_APP_TFT_DC_GPIO) |
                        (1ULL << CONFIG_APP_TFT_RST_GPIO) | (1ULL << CONFIG_APP_TFT_BLK_GPIO),
        .mode = GPIO_MODE_OUTPUT,
        .pull_up_en = GPIO_PULLUP_DISABLE,
        .pull_down_en = GPIO_PULLDOWN_DISABLE,
        .intr_type = GPIO_INTR_DISABLE,
    };
    gpio_config(&io);
    gpio_set_level(CONFIG_APP_TFT_BLK_GPIO, 1);
    gpio_set_level(CONFIG_APP_TFT_CS_GPIO, 1);
    gpio_set_level(CONFIG_APP_TFT_RST_GPIO, 1);
    vTaskDelay(pdMS_TO_TICKS(5));
    gpio_set_level(CONFIG_APP_TFT_RST_GPIO, 0);
    vTaskDelay(pdMS_TO_TICKS(20));
    gpio_set_level(CONFIG_APP_TFT_RST_GPIO, 1);
    vTaskDelay(pdMS_TO_TICKS(150));

    spi_bus_config_t bus = {
        .mosi_io_num = CONFIG_APP_TFT_MOSI_GPIO,
        .miso_io_num = -1,
        .sclk_io_num = CONFIG_APP_TFT_SCLK_GPIO,
        .quadwp_io_num = -1,
        .quadhd_io_num = -1,
        .max_transfer_sz = TFT_W * TFT_H * 2,
    };
    ESP_ERROR_CHECK(spi_bus_initialize(TFT_SPI_HOST, &bus, SPI_DMA_CH_AUTO));

    spi_device_interface_config_t dev = {
        .clock_speed_hz = 2000000,
        .mode = 0,
        .spics_io_num = -1,
        .queue_size = 1,
    };
    ESP_ERROR_CHECK(spi_bus_add_device(TFT_SPI_HOST, &dev, &s_tft_spi));

    tft_cmd(ST7735_SWRESET);
    vTaskDelay(pdMS_TO_TICKS(150));
    tft_cmd(ST7735_SLPOUT);
    vTaskDelay(pdMS_TO_TICKS(120));
    uint8_t colmod[] = {0x05};
    tft_cmd(ST7735_COLMOD);
    tft_data(colmod, sizeof(colmod));
    uint8_t madctl[] = {0xC0};
    tft_cmd(ST7735_MADCTL);
    tft_data(madctl, sizeof(madctl));
    tft_cmd(ST7735_INVON);
    tft_cmd(ST7735_NORON);
    vTaskDelay(pdMS_TO_TICKS(10));
    tft_cmd(ST7735_DISPON);
    vTaskDelay(pdMS_TO_TICKS(100));

    /* 诊断：先刷全屏红色确认 SPI 通信正常 */
    fb_fill(COLOR_RED);
    tft_flush();
    vTaskDelay(pdMS_TO_TICKS(800));

    /* 启动画面 */
    fb_fill(COLOR_DARK_BG);
    fb_draw_string_center(40, "AIoT Smart Window", COLOR_WHITE, COLOR_DARK_BG);
    fb_draw_string_center(56, "Starting up...", COLOR_GREEN, COLOR_DARK_BG);
    fb_draw_string_center(72, "SHT30+TVOC+LM393+MQ2", COLOR_GRAY, COLOR_DARK_BG);
    tft_flush();
    vTaskDelay(pdMS_TO_TICKS(1500));

    ESP_LOGI(TAG, "显示屏初始化完成");
    return ESP_OK;
}

/* ── 渲染 ──────────────────────────────────────────────────────────── */

esp_err_t display_render(const sensor_sample_t *sample, const fusion_state_t *state, const app_status_t *status) {
    if (!CONFIG_APP_DISPLAY_ENABLED) {
        return ESP_ERR_INVALID_STATE;
    }
    if (!sample || !state || !status) {
        return ESP_ERR_INVALID_ARG;
    }

    control_state_t control;
    control_state_get(&control);

    char buf[32];

    /* 空气质量 → 状态色 */
    uint16_t air_color = COLOR_GRAY;
    const char *air_text = "UNKNOWN";
    if (state->air_quality == FUSION_AIR_QUALITY_GOOD) {
        air_color = COLOR_GREEN;
        air_text = "GOOD";
    } else if (state->air_quality == FUSION_AIR_QUALITY_WATCH) {
        air_color = COLOR_YELLOW;
        air_text = "WATCH";
    } else if (state->air_quality == FUSION_AIR_QUALITY_ALERT) {
        air_color = COLOR_RED;
        air_text = "ALERT";
    }

    /* ── 清屏 + 标题栏 ── */
    fb_fill(COLOR_DARK_BG);
    fb_rect(0, 0, TFT_W, 17, COLOR_TITLE_BG);
    fb_draw_string_center(0, "= AIoT Monitor =", COLOR_WHITE, COLOR_TITLE_BG);

    /* ── 温湿度行 (y=18) ── */
    if (sample->climate_valid) {
        uint16_t tc = (sample->temperature_c > 32.0f)   ? COLOR_RED
                      : (sample->temperature_c > 28.0f) ? COLOR_YELLOW
                                                        : COLOR_GREEN;
        uint16_t hc =
            (sample->humidity_percent > 75.0f || sample->humidity_percent < 30.0f) ? COLOR_YELLOW : COLOR_GREEN;
        int tx = 4;
        fb_draw_string(tx, 18, "T:", COLOR_GRAY, COLOR_DARK_BG);
        tx += 16;
        snprintf(buf, sizeof(buf), "%.1fC", sample->temperature_c);
        fb_draw_string(tx, 18, buf, tc, COLOR_DARK_BG);
        tx += (int)strlen(buf) * 8;
        fb_draw_string(tx, 18, " H:", COLOR_GRAY, COLOR_DARK_BG);
        tx += 24;
        snprintf(buf, sizeof(buf), "%.1f%%", sample->humidity_percent);
        fb_draw_string(tx, 18, buf, hc, COLOR_DARK_BG);
    } else {
        fb_draw_string(4, 18, "T:--.-C H:--.-%", COLOR_GRAY, COLOR_DARK_BG);
    }

    /* ── TVOC / eCO2 行 (y=34) ── */
    if (sample->air_valid) {
        uint16_t vc = (sample->tvoc_ppb > 600) ? COLOR_RED : (sample->tvoc_ppb > 300) ? COLOR_YELLOW : COLOR_GREEN;
        uint16_t cc = (sample->eco2_ppm > 1500) ? COLOR_RED : (sample->eco2_ppm > 1000) ? COLOR_YELLOW : COLOR_GREEN;
        int tx = 4;
        fb_draw_string(tx, 34, "V:", COLOR_GRAY, COLOR_DARK_BG);
        tx += 16;
        snprintf(buf, sizeof(buf), "%u", sample->tvoc_ppb);
        fb_draw_string(tx, 34, buf, vc, COLOR_DARK_BG);
        tx += (int)strlen(buf) * 8;
        fb_draw_string(tx, 34, " C:", COLOR_GRAY, COLOR_DARK_BG);
        tx += 24;
        snprintf(buf, sizeof(buf), "%u", sample->eco2_ppm);
        fb_draw_string(tx, 34, buf, cc, COLOR_DARK_BG);
    } else {
        fb_draw_string(4, 34, "V:--- C:---", COLOR_GRAY, COLOR_DARK_BG);
    }

    /* ── HCHO / 光照行 (y=50) ── */
    {
        uint16_t fc = COLOR_GRAY;
        snprintf(buf, sizeof(buf), "---");
        if (sample->air_valid) {
            snprintf(buf, sizeof(buf), "%u", sample->hcho_ug_m3);
            fc = (sample->hcho_ug_m3 > 100) ? COLOR_RED : (sample->hcho_ug_m3 > 60) ? COLOR_YELLOW : COLOR_GREEN;
        }
        int tx = 4;
        fb_draw_string(tx, 50, "F:", COLOR_GRAY, COLOR_DARK_BG);
        tx += 16;
        fb_draw_string(tx, 50, buf, fc, COLOR_DARK_BG);
        tx += (int)strlen(buf) * 8;
        fb_draw_string(tx, 50, " L:", COLOR_GRAY, COLOR_DARK_BG);
        tx += 24;
        if (sample->light_valid) {
            fb_draw_string(tx, 50, sample->light_is_dark ? "DIM" : "BRT",
                           sample->light_is_dark ? COLOR_YELLOW : COLOR_GREEN, COLOR_DARK_BG);
        } else {
            fb_draw_string(tx, 50, "---", COLOR_GRAY, COLOR_DARK_BG);
        }
    }

    /* ── 烟雾行 (y=65) ── */
    {
        int tx = 4;
        fb_draw_string(tx, 65, "SMOKE:", COLOR_GRAY, COLOR_DARK_BG);
        tx += 48;
        if (sample->smoke_valid) {
            fb_draw_string(tx, 65, sample->smoke_detected ? "ALARM!" : "CLEAR",
                           sample->smoke_detected ? COLOR_RED : COLOR_GREEN, COLOR_DARK_BG);
        } else {
            fb_draw_string(tx, 65, "---", COLOR_GRAY, COLOR_DARK_BG);
        }
    }

    /* ── 分隔线 ── */
    fb_hline(0, 83, TFT_W, COLOR_LINE);

    /* ── 空气质量状态面板 (y=84, h=18) ── */
    {
        char status_line[20];
        snprintf(status_line, sizeof(status_line), "AIR: %s", air_text);
        int text_len = (int)strlen(status_line);
        int text_x = (TFT_W - text_len * 8) / 2;
        fb_rect(0, 84, TFT_W, 18, COLOR_PANEL_BG);
        fb_draw_string(text_x, 85, status_line, air_color, COLOR_PANEL_BG);
    }

    /* ── 窗户状态面板 (y=104, h=18) ── */
    {
        fb_rect(0, 104, TFT_W, 18, COLOR_PANEL_BG);
        if (control.manual_override) {
            fb_draw_string_center(105, control.manual_open ? "[MANUAL] OPEN" : "[MANUAL] CLOSED", COLOR_YELLOW,
                                  COLOR_PANEL_BG);
        } else {
            fb_draw_string_center(105, control.window_open ? "[AUTO] OPEN" : "[AUTO] CLOSED",
                                  control.window_open ? COLOR_GREEN : COLOR_GRAY, COLOR_PANEL_BG);
        }
    }

    /* ── MQTT 状态 (y=123) ── */
    fb_draw_string_center(123, status->cloud == APP_STATUS_LINK_READY ? "MQTT: ONLINE" : "MQTT: --",
                          status->cloud == APP_STATUS_LINK_READY ? COLOR_BLUE : COLOR_GRAY, COLOR_DARK_BG);

    tft_flush();
    return ESP_OK;
}
